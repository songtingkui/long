/***********************************************************************
函数功能:         DTMF初始化
输入参数:         无

输出参数:         无
函数返回值说明:
使用的资源:    	  无
***********************************************************************/
void send_dtmf_init(void)
{
    INT32U freq, cfg, rate, sum_width;

    // 注意保持VPWM位默认端口
    freq   = (*(volatile unsigned int *)0xbf0201b0) * 1000UL;
    freq <<= 2;                                                 // busclk * 4 = osc clk

    // 采样率需要接近58K：* 2 4 8
    rate = 22050 * 2;
    sum_width = (freq + rate - 1) / rate;

    //   PCM: 16bit, 22050, 1: 0x01A20000
    // ADPCM: 16bit, 22050, 1: IMA, 0x01A30000
    cfg = 0x01A30000 | (sum_width << 4);
    VPWM_CFG = cfg;
}

/***********************************************************************
函数功能:         DTMF缓冲区发送
输入参数:         无

输出参数:         无
函数返回值说明:
使用的资源:    	  无
***********************************************************************/
void send_dtmf_string(INT8U *dtmf, INT32U len)
{
    INT32U i, j, *audio_ptr;

    // 关闭接收
    PMU_ExintEn = 0;

    // 发送
    for (j = 0; j < len; j++)
    {
        // DTMF数据缓冲区
        audio_ptr = (INT32U *)DTMF_ADPCM_DATA[dtmf[j] & 0x0F];

        // ADPCM首字节
        VPWM_WPORT_STATUS = 2;
	    for (i = 0; i < (DTMF_CODE_LEN / 4); i++)
        {
            while ((VPWM_WPORT_STATUS & 0x01) == 0);
            VPWM_WPORT = *audio_ptr++;
        }

        // 不喂狗的话会复位, 5秒
        g_pmu->WdtFeed = WDTFEED_FOOD;
        delay_ms(5);
    }
    
    // 使能接收
    g_u32usartcommt = 0;
    PMU_ExintCLr = bit(15);
    PMU_ExintEn  = bit(15);
}

